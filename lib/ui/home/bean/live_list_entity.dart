class LiveListInfo {



	String page;
	List<LiveListInfoList> xList;

	LiveListInfo({this.page, this.xList});

	LiveListInfo.fromJson(Map<String, dynamic> json) {
		page = json['page'];
		if (json['list'] != null) {
			xList = new List<LiveListInfoList>();(json['list'] as List).forEach((v) { xList.add(new LiveListInfoList.fromJson(v)); });
		}
	}

	Map<String, dynamic> toJson() {
		final Map<String, dynamic> data = new Map<String, dynamic>();
		data['page'] = this.page;
		if (this.xList != null) {
      data['list'] =  this.xList.map((v) => v.toJson()).toList();
    }
		return data;
	}

	@override
	String toString() {
		return 'LiveListInfo{page: $page, xList: $xList}';
	}

}

