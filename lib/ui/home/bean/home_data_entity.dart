class HomeDataEntity {




	List<HomeDataUser> user;
	List<HomeDataBanner> banner;
	List<HomeDataNoticelist> noticelist;
	HomeDataDrugsrecommend drugsRecommend;
	HomeDatamedical  medical;
	List<HomeDatamedicalNews> medicalNews;


	HomeDataEntity({this.user, this.banner, this.noticelist, this.drugsRecommend, this. medical, this.medicalNews});

	HomeDataEntity.fromJson(Map<String, dynamic> json) {
		if (json['User'] != null) {
			user = new List<HomeDataUser>();(json['User'] as List).forEach((v) { user.add(new HomeDataUser.fromJson(v)); });
		}
		if (json['banner'] != null) {
			banner = new List<HomeDataBanner>();(json['banner'] as List).forEach((v) { banner.add(new HomeDataBanner.fromJson(v)); });
		}
		if (json['noticelist'] != null) {
			noticelist = new List<HomeDataNoticelist>();(json['noticelist'] as List).forEach((v) { noticelist.add(new HomeDataNoticelist.fromJson(v)); });
		}

		if (json['medical_news'] != null) {
			medicalNews = new List<HomeDatamedicalNews>();(json['medical_news'] as List).forEach((v) { medicalNews.add(new HomeDatamedicalNews.fromJson(v)); });
		}

		drugsRecommend = json['DrugsRecommend'] != null ? new HomeDataDrugsrecommend.fromJson(json['DrugsRecommend']) : null;
		 medical = json[' medical'] != null ? new HomeDatamedical.fromJson(json[' medical']) : null;

	}

	Map<String, dynamic> toJson() {
		final Map<String, dynamic> data = new Map<String, dynamic>();
		if (this.user != null) {
      data['User'] =  this.user.map((v) => v.toJson()).toList();
    }
		if (this.banner != null) {
      data['banner'] =  this.banner.map((v) => v.toJson()).toList();
    }
		if (this.noticelist != null) {
      data['noticelist'] =  this.noticelist.map((v) => v.toJson()).toList();
    }

		if (this.medicalNews != null) {
			data['medical_news'] =  this.medicalNews.map((v) => v.toJson()).toList();
		}

		if (this.drugsRecommend != null) {
      data['DrugsRecommend'] = this.drugsRecommend.toJson();
    }
		if (this. medical != null) {
      data[' medical'] = this. medical.toJson();
    }
		return data;
	}

	@override
	String toString() {
		return 'HomeDataEntity{user: $user, banner: $banner, noticelist: $noticelist, drugsRecommend: $drugsRecommend, medical: $medical, medicalNews: $medicalNews}';
	}
}

